// Utility functions for design system
export const getColor = (color: string, shade: number = 500) => {
  const [colorName] = color.split('-')
  return designSystem.colors[colorName as keyof typeof designSystem.colors]?.[shade as keyof typeof designSystem.colors.primary]
}

export const getSpacing = (size: number | string) => {
  return designSystem.spacing[size as keyof typeof designSystem.spacing]
}

export const getFontSize = (size: string) => {
  return designSystem.typography.fontSize[size as keyof typeof designSystem.typography.fontSize]
}